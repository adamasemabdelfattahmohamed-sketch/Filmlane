/** تحويل الأرقام الهندية إلى عربية (غربية) */
export const convertHindiToArabic = (text: string): string => {
  const hindiDigits = "٠١٢٣٤٥٦٧٨٩";
  return text.replace(/[٠-٩]/g, (d) => String(hindiDigits.indexOf(d)));
};

